create table emp(
       empno  int  primary key, 
       ename varchar(10), 
       job varchar(9), 
       mgr int, 
       hiredate date, 
       salary float, 
       comm float, 
       deptno int 
);
 
  commit;

alter table emp add constraint fk_emp_dept foreign key(deptno) references dept(deptno);


